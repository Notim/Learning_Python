import math

print("Tipos de operacoes aritimeticas")
print("EXEMPLO x= 10 e y = 20\n")
print("numeros negativos -x: ", -10)
print("soma x + y: ", 10 + 20)
print("subtracao  x - y: ", 10 - 20)
print("multiplicacao x * y: ", 10 * 20)
print("divisao com saida float x / y: ", 10 / 20)
print("divisao com saida truncada(ignora os pontos decimais e não necessariamente a saida é inteira) x // y: ", 10 // 20)
print("modulo ou resto da divisao x % 2: ", 10 % 2)
print("exponenciacao x ** 2", 10 ** 2)
print("contante e: ",math.e)
print("retorna o fatorial de x!: ",math.factorial(10))
print("logaritimo de log y por x: ",math.log(10,20))
print("logaritimo de log 10 por x: ",math.log10(10))

